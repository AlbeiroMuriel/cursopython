# 1.	Solicitar tres números cualquiera y escribir la suma y la multiplicación de los números.

a = int(input("Ingrese Nro 1 --> "))
b = int(input("Ingrese Nro 2 --> "))
c = int(input("Ingrese Nro 3 --> "))
suma = a + b + c
mult = a * b * c

print(f'El Nro 1 es --> {a} El Nro 2 es --> {b} El Nro 3 es --> {c}  La SUMA ES {suma}')
print(f'El Nro 1 es --> {a} El Nro 2 es --> {b} El Nro 3 es --> {c}  La MULTIPLICACION  ES {mult}')


